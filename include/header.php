<script>
const toggle = document.querySelector('.mobile-toggle');
const menu = document.querySelector('.menu');

// Toggle mobile menu
toggle.addEventListener('click', () => {
  if (menu.style.display === 'flex') {
    menu.style.display = 'none';
  } else {
    menu.style.display = 'flex';
  }
});

// Mobile dropdown toggle
const dropdownParents = document.querySelectorAll('.has-dropdown');
dropdownParents.forEach(parent => {
  const link = parent.querySelector('a');

  // Toggle dropdown on parent click
  link.addEventListener('click', function(e) {
    if(window.innerWidth <= 992){
      e.preventDefault();
      parent.classList.toggle('open');

      const dropdown = parent.querySelector('.dropdown');
      if(dropdown){
        dropdown.style.display = parent.classList.contains('open') ? 'block' : 'none';
        
        // Auto scroll Abu Dhabi dropdown to top when opened on mobile
        if (dropdown.classList.contains('abu-dhabi-dropdown')) {
          setTimeout(() => {
            dropdown.scrollTop = 0;
          }, 100);
        }
      }
    }
  });

  // Close dropdown when a child link is clicked
  const childLinks = parent.querySelectorAll('.dropdown li a');
  childLinks.forEach(child => {
    child.addEventListener('click', function() {
      if(window.innerWidth <= 992){
        // Collapse the parent dropdown
        parent.classList.remove('open');
        const dropdown = parent.querySelector('.dropdown');
        if(dropdown){
          dropdown.style.display = 'none';
        }
        // Close the whole mobile menu
        menu.style.display = 'none';
      }
    });
  });
});

// Sticky navbar after scroll
const navbar = document.getElementById('mainNavbar');
const placeholder = document.getElementById('navbar-placeholder');
let normalHeight = navbar.offsetHeight;

window.addEventListener('scroll', () => {
  if(window.scrollY > 50){
    navbar.classList.add('sticky');
    placeholder.style.height = normalHeight + 'px';
  } else {
    navbar.classList.remove('sticky');
    placeholder.style.height = '0';
  }
});

window.addEventListener('resize', function() {
  normalHeight = navbar.offsetHeight;
});

// Auto scroll Abu Dhabi dropdown to top when hovered on desktop
const abuDhabiLinks = document.querySelectorAll('.abu-dhabi-services-link');
abuDhabiLinks.forEach(link => {
    link.addEventListener('mouseenter', function() {
        const dropdown = this.nextElementSibling;
        if (dropdown && dropdown.classList.contains('abu-dhabi-dropdown')) {
            setTimeout(() => {
                dropdown.scrollTop = 0;
            }, 50);
        }
    });
});

// Fix for desktop hover dropdown display
document.addEventListener('DOMContentLoaded', function() {
  // Ensure Abu Dhabi dropdown has proper display on hover
  const style = document.createElement('style');
  style.innerHTML = `
    .has-dropdown:hover > .dropdown.abu-dhabi-dropdown {
      display: block !important;
      overflow-y: auto !important;
    }
  `;
  document.head.appendChild(style);
});
</script>