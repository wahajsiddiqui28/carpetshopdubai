        // Slider functionality
        document.addEventListener('DOMContentLoaded', function() {
            const sliderTrack = document.querySelector('.slider-track');
            const slides = document.querySelectorAll('.slide');
            const prevBtn = document.querySelector('.prev-btn');
            const nextBtn = document.querySelector('.next-btn');
            const dots = document.querySelectorAll('.dot');
            let currentSlide = 0;
            if (sliderTrack) {
                function updateSlider() {
                    sliderTrack.style.transform = `translateX(-${currentSlide * 100}%)`;
                    dots.forEach((d, i) => d.classList.toggle('active', i === currentSlide));
                }
                nextBtn?.addEventListener('click', function() {
                    currentSlide = (currentSlide + 1) % slides.length;
                    updateSlider();
                });
                prevBtn?.addEventListener('click', function() {
                    currentSlide = (currentSlide - 1 + slides.length) % slides.length;
                    updateSlider();
                });
                dots.forEach((dot, i) => dot.addEventListener('click', function() {
                    currentSlide = i;
                    updateSlider();
                }));
                setInterval(() => {
                    currentSlide = (currentSlide + 1) % slides.length;
                    updateSlider();
                }, 5000);
            }

            // Filter buttons for carpet collection
            const filterBtns = document.querySelectorAll('.carpet-filters .filter-btn');
            const cards = document.querySelectorAll('.carpet-card');
            filterBtns.forEach(btn => {
                btn.addEventListener('click', function() {
                    filterBtns.forEach(b => b.classList.remove('active'));
                    this.classList.add('active');
                    const filter = this.dataset.filter;
                    cards.forEach(card => {
                        if (filter === 'all' || card.dataset.category === filter) card.style.display = 'block';
                        else card.style.display = 'none';
                    });
                });
            });

            // Quick View modal
            const modal = document.querySelector('.quick-view-modal');
            const modalBody = document.querySelector('.modal-body');
            document.querySelectorAll('.quick-view, .details-btn').forEach(el => {
                el.addEventListener('click', function(e) {
                    const card = this.closest('.carpet-card');
                    if (!card) return;
                    const img = card.querySelector('.carpet-image').src;
                    const name = card.querySelector('.carpet-name').textContent;
                    const code = card.querySelector('.carpet-code').textContent;
                    modalBody.innerHTML = `<div style="text-align:center;"><img src="${img}" style="width:100%; max-height:300px; object-fit:cover; border-radius:10px;"><h2 style="margin-top:20px;">${name}</h2><p style="color:#555;">Product Code: ${code}</p><p style="color:#000;">Premium quality, stain resistant, available in custom sizes. Contact us for best price.</p><a href="http://wa.me/971565847696?text=Hi, I want to order ${name} (${code})" class="order-now-btn" style="display:inline-block; margin-top:20px; padding:12px 30px; background:#25D366;">Chat on WhatsApp</a></div>`;
                    modal.style.display = 'flex';
                });
            });
            document.querySelector('.modal-close')?.addEventListener('click', () => modal.style.display = 'none');
            modal?.addEventListener('click', e => {
                if (e.target === modal) modal.style.display = 'none';
            });

            // View more (simple toggle)
            let viewMore = document.querySelector('.view-more-btn');
            let hiddenCards = Array.from(cards).slice(8);
            if (viewMore) {
                hiddenCards.forEach(c => c.style.display = 'none');
                viewMore.addEventListener('click', function() {
                    if (this.innerHTML.includes('More')) {
                        hiddenCards.forEach(c => c.style.display = 'block');
                        this.innerHTML = 'Show Less <i class="fas fa-arrow-up"></i>';
                    } else {
                        hiddenCards.forEach(c => c.style.display = 'none');
                        this.innerHTML = 'View More Products <i class="fas fa-arrow-down"></i>';
                    }
                });
            }

            // Claim offer btn
            document.querySelector('.offer-btn')?.addEventListener('click', () => window.open('http://wa.me/971565847696?text=Hi, I want to claim the 30% OFF offer on living room carpet', '_blank'));
        });

        // Tab function from original
        function openTab(evt, tabName) {
            var tabContents = document.getElementsByClassName('tab-content');
            for (var i = 0; i < tabContents.length; i++) tabContents[i].classList.remove('active');
            var tabBtns = document.getElementsByClassName('tab-button');
            for (var i = 0; i < tabBtns.length; i++) tabBtns[i].classList.remove('active');
            document.getElementById(tabName).classList.add('active');
            evt.currentTarget.classList.add('active');
