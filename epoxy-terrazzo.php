                                    <h3>Caring for Your Epoxy Terrazzo Floor</h3>

                                    <p>Epoxy terrazzo is one of the lowest-maintenance floor finishes available, but a simple routine will keep it looking exceptional for decades:</p>

                                    <ul class="tile-list">
                                        <li><strong>Daily:</strong> Dust-mop or sweep to remove grit particles that could scratch the polished surface under foot traffic.</li>
                                        <li><strong>Weekly:</strong> Damp mop with warm water and a pH-neutral cleaner. Avoid acidic or alkaline cleaners, which can dull the sealer finish over time.</li>
                                        <li><strong>Monthly:</strong> Inspect for any areas where the sealer appears to be losing its sheen under heavy traffic and address early.</li>
                                        <li><strong>Every 2 to 3 years:</strong> Apply a fresh coat of penetrating sealer to maintain the surface protection and refresh the gloss.</li>
                                        <li><strong>Every 10 to 15 years:</strong> A professional re-grind and re-polish can return any epoxy terrazzo floor to its original installed condition, making it one of the most renewable flooring systems available.</li>
                                    </ul>

