                                    <h3>The Epoxy Terrazzo Installation Process</h3>

                                    <div class="process-steps">
                                        <div class="process-step">
                                            <div class="step-number">1</div>
                                            <div class="step-content">
                                                <h4>Design Consultation and Sample Board</h4>
                                                <p>Every epoxy terrazzo project begins with a design session. We present aggregate samples, chip size options, matrix pigment colours, and surface finish choices. Once a design direction is agreed, we prepare a physical sample board using your exact aggregate and matrix specification, so you can see and touch the real result before any floor work begins. Amendments can be made at this stage with no cost or delay implications.</p>
                                            </div>
                                        </div>
                                        <div class="process-step">
                                            <div class="step-number">2</div>
                                            <div class="step-content">
                                                <h4>Substrate Preparation</h4>
                                                <p>The concrete base is diamond-ground to remove laitance, existing sealers, and surface contamination. Any cracks, control joints, or weak areas are cut out and repaired with epoxy mortar. The surface is then profiled to the correct roughness for maximum bond strength. Moisture testing is carried out and any readings above the acceptable threshold are addressed with a moisture-tolerant primer system.</p>
                                            </div>
                                        </div>
                                        <div class="process-step">
                                            <div class="step-number">3</div>
                                            <div class="step-content">
                                                <h4>Priming</h4>
                                                <p>A low-viscosity, zero-VOC epoxy primer is roller-applied across the prepared substrate. The primer penetrates the concrete pores, seals the surface against outgassing, and provides a chemically compatible base for the terrazzo matrix to bond to. Full primer cure is confirmed before proceeding.</p>
                                            </div>
                                        </div>
                                        <div class="process-step">
                                            <div class="step-number">4</div>
                                            <div class="step-content">
                                                <h4>Divider Strip Installation (Where Specified)</h4>
                                                <p>If the design includes geometric patterns, medallions, company logos, or colour zone separations, brass, aluminium, or zinc divider strips are fixed to the primed substrate at this stage. These create the outlines of each design element and separate different aggregate mixes within the same floor.</p>
                                            </div>
                                        </div>
                                        <div class="process-step">
                                            <div class="step-number">5</div>
                                            <div class="step-content">
                                                <h4>Terrazzo Matrix Application</h4>
                                                <p>The aggregate-epoxy mixture is prepared in accurately measured batches to ensure consistency of aggregate loading and matrix colour across the entire floor. The mixture is trowel-applied to the substrate and compacted to remove voids. Multiple passes ensure even aggregate distribution and correct system thickness throughout.</p>
                                            </div>
                                        </div>
                                        <div class="process-step">
                                            <div class="step-number">6</div>
                                            <div class="step-content">
                                                <h4>Initial Grind</h4>
                                                <p>After a minimum curing period of 24 to 48 hours, the surface is ground with coarse diamond tooling to flatten the matrix surface and fully expose the aggregate faces. This grind removes the resin skin over the chips and reveals the decorative aggregate that gives the floor its character.</p>
                                            </div>
                                        </div>
                                        <div class="process-step">
                                            <div class="step-number">7</div>
                                            <div class="step-content">
                                                <h4>Progressive Polishing</h4>
                                                <p>Further grinding passes using progressively finer diamond grits refine the surface. Each pass removes the scratch marks from the previous one. The number of grits used depends on the specified finish: a honed commercial floor requires fewer passes than a mirror-polished residential floor.</p>
                                            </div>
                                        </div>
                                        <div class="process-step">
                                            <div class="step-number">8</div>
                                            <div class="step-content">
                                                <h4>Sealing and Final Finish</h4>
                                                <p>A penetrating epoxy or polyurethane sealer is applied to close the polished surface. For high-gloss or mirror finish requirements, a final crystallisation or burnishing treatment is applied. The floor is cleaned and protected before client handover.</p>
                                            </div>
                                        </div>
                                    </div>

